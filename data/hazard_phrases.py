# ─── Funzioni di utilità ──────────────────────────────────────────────────────

def _extract_code(phrase_string: str) -> str | None:
    """
    Estrae il codice di frase da una stringa PubChem.
    Esempi:
      'H225 Highly flammable liquid and vapour'  → 'H225'
      'H300+H310+H330 Fatal if swallowed...'     → 'H300+H310+H330'
      'EUH014 Reacts violently with water.'      → 'EUH014'
    """
    m = re.match(r'^((?:EUH|H|P)\d+(?:[+/](?:EUH|H|P)\d+)*)', phrase_string.strip())
    return m.group(1) if m else None


def translate_phrase(phrase_string: str, lang: str) -> dict:
    """
    Restituisce un dizionario {'code': ..., 'text': ...} per la lingua richiesta.
    Se la traduzione non è disponibile, torna al testo inglese di PubChem.
    """
    code = _extract_code(phrase_string)
    if not code:
        return {"code": None, "text": phrase_string}

    if lang == "en":
        # Usa il testo già incorporato nella stringa PubChem come fallback,
        # oppure il dizionario EN per uniformità.
        parts = re.split(r'[+/]', code)
        texts = [H_EN.get(p) or P_EN.get(p) for p in parts]
        if all(texts):
            return {"code": code, "text": " / ".join(t for t in texts if t)}
        # fallback: testo grezzo di PubChem senza il codice
        raw = re.sub(r'^(?:EUH|H|P)\d+(?:[+/](?:EUH|H|P)\d+)*\s*', '', phrase_string).strip()
        return {"code": code, "text": raw or phrase_string}

    else:  # it
        parts = re.split(r'[+/]', code)
        texts_it = [H_IT.get(p) or P_IT.get(p) for p in parts]
        if all(texts_it):
            return {"code": code, "text": " / ".join(t for t in texts_it if t)}
        # fallback inglese
        return translate_phrase(phrase_string, "en")


def translate_phrases(phrases: list, lang: str) -> list:
    """Traduce una lista di frasi; restituisce lista di dict {'code', 'text'}."""
    return [translate_phrase(p, lang) for p in phrases]
